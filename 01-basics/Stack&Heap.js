/*
1. Primitive Data Types (Stored in the Stack)
Definition: Basic types that hold a single, simple value.

Immutability: They are immutable (the original value itself cannot be changed).

Assignment: Copied by value. If you copy a variable, changing the new one will not affect the original.

Types (7): String, Number, Boolean, Undefined, Null, Symbol, BigInt.

2. Non-Primitive / Reference Types (Stored in the Heap)
Definition: Complex types used to store collections of data or multiple values.

Mutability: They are mutable (you can modify their contents or properties).

Assignment: Copied by reference. If you copy a variable, both point to the same memory address. Changing one will affect the other.

Types: Object, Array, Function.
*/